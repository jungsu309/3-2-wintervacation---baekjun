import java.util.Scanner;
public class Main{
    public static void main(String[] args){
        int A,B,C;
        Scanner s= new Scanner(System.in);
        while(s.hasNextInt()){
            A = s.nextInt();
            B = s.nextInt();
            C = A+B;
            System.out.println(C);
        }
    }
}